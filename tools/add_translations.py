def generate_mac_strings_xml(lang_stem):
    """Generate <MacStrings> XML block for a given language."""
    translations = TRANSLATIONS.get(lang_stem, {})

    lines = ['\t\t\t<MacStrings>']
    for str_id, english in STRINGS:
        translated = translations.get(str_id, english)  # fallback to English
        # XML-escape
        translated = translated.replace('&', '&amp;').replace('"', '&quot;').replace('<', '&lt;').replace('>', '&gt;')
        lines.append(f'\t\t\t\t<Item id="{str_id}" name="{translated}"/>')
    lines.append('\t\t\t</MacStrings>')
    return '\n'.join(lines)


def insert_mac_strings(xml_path, lang_stem):
    """Insert or replace <MacStrings> in a language XML file."""
    with open(xml_path, 'r', encoding='utf-8') as f:
        content = f.read()

    mac_xml = generate_mac_strings_xml(lang_stem)

    # Check if <MacStrings> already exists
    if '<MacStrings>' in content:
        # Replace existing
        pattern = r'\s*<MacStrings>.*?</MacStrings>'
        content = re.sub(pattern, '\n' + mac_xml, content, flags=re.DOTALL)
    else:
        # Insert before </Dialog> or before </Native-Langue>
        if '</Dialog>' in content:
            content = content.replace('</Dialog>', mac_xml + '\n\t\t</Dialog>')
        elif '</Native-Langue>' in content:
            content = content.replace('</Native-Langue>',
                '\t<Dialog>\n' + mac_xml + '\n\t\t</Dialog>\n\t</Native-Langue>')
        else:
            print(f"  WARNING: Could not find insertion point in {xml_path}")
            return False

    with open(xml_path, 'w', encoding='utf-8') as f:
        f.write(content)
    return True


def main():
    loc_dir = os.path.join(os.path.dirname(os.path.dirname(os.path.abspath(__file__))),
                           'resources', 'localization')

    if not os.path.isdir(loc_dir):
        print(f"Error: localization directory not found: {loc_dir}")
        sys.exit(1)

    xml_files = sorted([f for f in os.listdir(loc_dir) if f.endswith('.xml')])
    print(f"Found {len(xml_files)} language files in {loc_dir}")
    print(f"Inserting {len(STRINGS)} macOS-specific string translations...\n")

    success = 0
    failed = 0

    for xml_file in xml_files:
        lang_stem = os.path.splitext(xml_file)[0]
        xml_path = os.path.join(loc_dir, xml_file)

        has_translation = lang_stem in TRANSLATIONS
        label = "translated" if has_translation else "English fallback"

        if insert_mac_strings(xml_path, lang_stem):
            print(f"  ✓ {xml_file} ({label})")
            success += 1
        else:
            print(f"  ✗ {xml_file} FAILED")
            failed += 1

    print(f"\nDone: {success} updated, {failed} failed")
    print(f"Languages with full translations: {len(TRANSLATIONS)}")
    print(f"Languages with English fallback: {success - len(TRANSLATIONS)}")


if __name__ == '__main__':
    main()
